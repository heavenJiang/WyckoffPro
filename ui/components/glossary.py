def tip(key: str) -> str:
    """返回术语解释，用于 Streamlit help= 参数（自动换行）"""
    return GLOSSARY.get(key, "")


def md_tip(display: str, key: str = None) -> str:
    """
    返回带 HTML title tooltip 的 HTML 片段，用于 st.markdown(unsafe_allow_html=True)。
    示例：st.markdown(md_tip("点数图"), unsafe_allow_html=True)
    """
    k = key or display
    definition = GLOSSARY.get(k, "")
    if not definition:
        return display
    # 替换换行为空格，避免 title 属性中的换行问题
    safe_def = definition.replace('"', "'").replace("\n", " | ")
    return (
        f'<span title="{safe_def}" style="'
        f'border-bottom: 1px dashed #aaa; cursor: help;">'
        f'{display}</span>'
    )


